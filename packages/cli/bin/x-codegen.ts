import { createHash } from 'node:crypto'
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { emitRegistry, emitShims, emitToolCallers, emitToolTypes } from '../src/codegen/emit'
import { collect } from '../src/codegen/scan'
import { extractSchemas } from '../src/codegen/schema'
const PLATFORM_ROOT = resolve(process.cwd(), 'convex/tools')
const APPS_DIR = resolve(process.cwd(), '../')
const APPS_MANIFEST_OUT = resolve(process.cwd(), 'convex/apps/_apps.ts')
const discoverAppsForManifest = async (): Promise<string[]> => {
  const out: string[] = []
  const entries = await readdir(APPS_DIR, { withFileTypes: true })
  for (const e of entries) {
    if (!e.isDirectory()) continue
    if (e.name === 'backend') continue
    try {
      await readdir(resolve(APPS_DIR, e.name, 'server'))
    } catch {
      continue
    }
    out.push(e.name)
  }
  return out.toSorted()
}
const emitAppsManifest = (apps: string[]): string => {
  const imports = apps.map(a => `import { config as ${a} } from '../../../${a}/server'`).join('\n')
  const map = apps.join(', ')
  return `// GENERATED by packages/cli/bin/x-codegen.ts — do not edit by hand.
${imports}
const APPS = { ${map} } as const
export { APPS }
`
}
const discoverAppRoots = async (): Promise<{ importBase: string; root: string }[]> => {
  const out: { importBase: string; root: string }[] = []
  const entries = await readdir(APPS_DIR, { withFileTypes: true })
  for (const e of entries) {
    if (!e.isDirectory()) continue
    if (e.name === 'backend') continue
    const root = resolve(APPS_DIR, e.name, 'server/tools')
    try {
      const stat = await readdir(root)
      if (stat.length === 0) continue
    } catch {
      continue
    }
    out.push({ importBase: `${e.name}/server/tools/`, root })
  }
  return out
}
const GEN_DIR = resolve(PLATFORM_ROOT, 'generated')
const SHIMS_DIR = PLATFORM_ROOT
const OUT = resolve(GEN_DIR, 'registry.ts')
const TOOL_TYPES_OUT = resolve(GEN_DIR, 'toolTypes.ts')
const TOOL_CALLERS_OUT = resolve(GEN_DIR, 'toolCallers.ts')
const HASHES_OUT = resolve(GEN_DIR, 'schemaHashes.json')
const hashSchema = (s: unknown): string => createHash('sha256').update(JSON.stringify(s)).digest('hex').slice(0, 12)
const diffHashes = async (
  path: string,
  next: Record<string, string>
): Promise<{ added: string[]; changed: string[]; removed: string[] }> => {
  let prev: Record<string, string> = {}
  try {
    prev = JSON.parse(await readFile(path, 'utf8')) as Record<string, string>
  } catch {
    //
  }
  const added: string[] = []
  const changed: string[] = []
  const removed: string[] = []
  for (const [k, v] of Object.entries(next))
    if (!(k in prev)) added.push(k)
    else if (prev[k] !== v) changed.push(k)
  for (const k of Object.keys(prev)) if (!(k in next)) removed.push(k)
  return { added, changed, removed }
}
const main = async (): Promise<void> => {
  const appRoots = await discoverAppRoots()
  const specs: { importBase: null | string; root: string }[] = [...appRoots, { importBase: null, root: PLATFORM_ROOT }]
  const data = await collect(specs, GEN_DIR, SHIMS_DIR)
  if (data.tools.length === 0) {
    console.error(`no tool files found under any of: ${specs.map(s => s.root).join(', ')}`)
    process.exit(1)
  }
  console.log(`extracting handler return types for ${data.tools.length} tools via ts…`)
  const schemas = extractSchemas(data.tools.map(t => t.absPath))
  await mkdir(dirname(OUT), { recursive: true })
  await writeFile(OUT, emitRegistry({ ...data, outDir: GEN_DIR, schemas, shimsDir: SHIMS_DIR }))
  await writeFile(TOOL_TYPES_OUT, emitToolTypes(data.tools, schemas))
  await writeFile(TOOL_CALLERS_OUT, emitToolCallers(data.tools, schemas))
  if (SHIMS_DIR) {
    const shims = emitShims(data.tools, SHIMS_DIR)
    await Promise.all(
      shims.map(async s => {
        await mkdir(dirname(s.absPath), { recursive: true })
        await writeFile(s.absPath, s.source)
      })
    )
    console.log(`shims: ${shims.length} files written under ${SHIMS_DIR}`)
  }
  const hashes: Record<string, string> = {}
  for (const t of data.tools) {
    const ex = schemas.get(t.absPath)
    if (ex) hashes[t.cliPath.join('.')] = hashSchema({ args: ex.args, schema: ex.schema })
  }
  const drift = await diffHashes(HASHES_OUT, hashes)
  await writeFile(HASHES_OUT, `${JSON.stringify(hashes, null, 2)}\n`)
  if (drift.added.length + drift.changed.length + drift.removed.length > 0) {
    console.log('\nschema drift:')
    for (const k of drift.added) console.log(`  + ${k}`)
    for (const k of drift.changed) console.log(`  ~ ${k}`)
    for (const k of drift.removed) console.log(`  - ${k}`)
  }
  console.log(`registry: ${data.providers.length} providers, ${data.tools.length} tools`)
  for (const t of data.tools) console.log(`  ${t.tier === 'admin' ? '[admin] ' : ''}${t.cliPath.join(' ')}`)
  const appNames = await discoverAppsForManifest()
  await mkdir(dirname(APPS_MANIFEST_OUT), { recursive: true })
  await writeFile(APPS_MANIFEST_OUT, emitAppsManifest(appNames))
  console.log(`apps manifest: ${appNames.length} apps (${appNames.join(', ')})`)
}
await main()
