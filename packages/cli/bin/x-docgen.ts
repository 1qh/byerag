#!/usr/bin/env bun
/* eslint-disable no-console */
/** biome-ignore-all lint/performance/noAwaitInLoops: small N (apps), fs IO ordered */
/* eslint-disable no-await-in-loop */
import { mkdir, readdir, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import type { ExtractedMeta } from '../src/codegen/extract-meta'
import { extractMeta } from '../src/codegen/extract-meta'
import { collect } from '../src/codegen/scan'

const APPS_DIR = resolve(process.cwd(), '../')
const PROVIDER_PREFIX_RE = /^_/u
const byString = (a: string, b: string): number => (a < b ? -1 : Number(a > b))
const mdRow = (cells: string[]): string => `| ${cells.join(' | ')} |`
const escapeMd = (s: string): string => s.replaceAll('|', String.raw`\|`).replaceAll('\n', ' ')
const discoverApps = async (): Promise<{ name: string; root: string }[]> => {
  const out: { name: string; root: string }[] = []
  const entries = await readdir(APPS_DIR, { withFileTypes: true })
  for (const e of entries)
    if (e.isDirectory() && e.name !== 'backend') {
      const root = resolve(APPS_DIR, e.name, 'server/tools')
      try {
        const stat = await readdir(root)
        if (stat.length > 0) out.push({ name: e.name, root })
      } catch {
        /* No tools dir — skip */
      }
    }
  return out
}
const pushToolDetail = (lines: string[], t: { cliPath: string[]; meta: ExtractedMeta; tier: 'admin' | 'user' }): void => {
  if (t.meta.examples.length === 0 && Object.keys(t.meta.argDescriptions).length === 0) return
  lines.push(`### \`${t.cliPath.join(' ').replace(PROVIDER_PREFIX_RE, '')}\``, '')
  if (Object.keys(t.meta.argDescriptions).length > 0) {
    lines.push('Args:', '')
    for (const [name, desc] of Object.entries(t.meta.argDescriptions)) lines.push(`- \`--${name}\` — ${escapeMd(desc)}`)
    lines.push('')
  }
  if (t.meta.examples.length > 0) {
    lines.push('Examples:', '', '```sh')
    for (const ex of t.meta.examples) lines.push(ex)
    lines.push('```', '')
  }
}
const renderInventory = (
  appName: string,
  tools: { cliPath: string[]; meta: ExtractedMeta; tier: 'admin' | 'user' }[]
): string => {
  const toolsByProvider = new Map<string, typeof tools>()
  for (const t of tools) {
    const key = t.cliPath[0] ?? 'unknown'
    if (!toolsByProvider.has(key)) toolsByProvider.set(key, [])
    toolsByProvider.get(key)?.push(t)
  }
  const now = new Date().toISOString().slice(0, 10)
  const lines: string[] = [
    `# Tool inventory — ${appName}`,
    '',
    '<!-- GENERATED by packages/cli/bin/x-docgen.ts — do not edit by hand. Re-run via `bun run codegen` in apps/backend. -->',
    '',
    `Generated ${now} · ${tools.length} tools across ${toolsByProvider.size} provider(s).`,
    ''
  ]
  const providers = [...toolsByProvider.keys()].toSorted(byString)
  for (const prov of providers) {
    const label = prov.replace(PROVIDER_PREFIX_RE, '')
    const list = (toolsByProvider.get(prov) ?? []).toSorted((a, b) =>
      a.cliPath.join(' ').localeCompare(b.cliPath.join(' '))
    )
    lines.push(
      `## \`${label}\``,
      '',
      mdRow(['Command', 'Description', 'Version', 'Deprecated']),
      mdRow(['---', '---', '---', '---'])
    )
    for (const t of list) {
      const cmd = `\`${t.cliPath.join(' ').replace(PROVIDER_PREFIX_RE, '')}\``
      lines.push(
        mdRow([
          cmd,
          escapeMd(t.meta.description || '—'),
          String(t.meta.version),
          t.meta.deprecated ? escapeMd(t.meta.deprecated) : ''
        ])
      )
    }
    lines.push('')
    for (const t of list) pushToolDetail(lines, t)
  }
  return `${lines.join('\n')}\n`
}
const main = async (): Promise<void> => {
  const apps = await discoverApps()
  if (apps.length === 0) {
    console.error(`no apps with server/tools found under ${APPS_DIR}; skipping docgen`)
    process.exitCode = 1
    return
  }
  for (const app of apps) {
    const data = await collect([{ importBase: null, root: app.root }], resolve(app.root, '__nogen__'))
    const metas = extractMeta(data.tools.map(t => t.absPath))
    const tools: { cliPath: string[]; meta: ExtractedMeta; tier: 'admin' | 'user' }[] = []
    for (const t of data.tools) {
      const m = metas.get(t.absPath)
      if (m) tools.push({ cliPath: t.cliPath, meta: m, tier: t.tier })
    }
    const out = resolve(APPS_DIR, app.name, 'server', 'INVENTORY.md')
    await mkdir(dirname(out), { recursive: true })
    await writeFile(out, renderInventory(app.name, tools))
    console.log(`${app.name}: wrote ${out} — ${tools.length} tools`)
  }
}
await main()
